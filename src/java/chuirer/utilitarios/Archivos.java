    public static File subeArchivo(HttpServletRequest request) throws FileUploadException, Exception {
        FileItemFactory factory = new DiskFileItemFactory();
        ServletFileUpload upload = new ServletFileUpload(factory);
        File TempDir = new File(new Propiedades().getProperties().getProperty("rutaImagenes"));
        List items = null;
        String idSesion = request.getSession().getId();
        items = upload.parseRequest(request);
        for (Object item : items) {
            FileItem uploaded = (FileItem) item;
            if (!uploaded.isFormField()) {
                String nombreImg = idSesion+uploaded.getName().substring(uploaded.getName().indexOf("."));
                File archivo = new File(TempDir.getAbsolutePath(), nombreImg);
                uploaded.write(archivo);
                return archivo;
            }
        }
        return null;
    }

